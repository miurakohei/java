package daodto;

public class TabyouinDTO {
	private String tabid;
	private String name;
	private String address;
	private String tel;
	private int sihon;
	private int kyukyu;
	/**
	 * tabidを取得します。
	 * @return tabid
	 */
	public String getTabid() {
	    return tabid;
	}
	/**
	 * tabidを設定します。
	 * @param tabid tabid
	 */
	public void setTabid(String tabid) {
	    this.tabid = tabid;
	}
	/**
	 * nameを取得します。
	 * @return name
	 */
	public String getName() {
	    return name;
	}
	/**
	 * nameを設定します。
	 * @param name name
	 */
	public void setName(String name) {
	    this.name = name;
	}
	/**
	 * addressを取得します。
	 * @return address
	 */
	public String getAddress() {
	    return address;
	}
	/**
	 * addressを設定します。
	 * @param address address
	 */
	public void setAddress(String address) {
	    this.address = address;
	}
	/**
	 * telを取得します。
	 * @return tel
	 */
	public String getTel() {
	    return tel;
	}
	/**
	 * telを設定します。
	 * @param tel tel
	 */
	public void setTel(String tel) {
	    this.tel = tel;
	}
	/**
	 * sihonを取得します。
	 * @return sihon
	 */
	public int getSihon() {
	    return sihon;
	}
	/**
	 * sihonを設定します。
	 * @param sihon sihon
	 */
	public void setSihon(int sihon) {
	    this.sihon = sihon;
	}
	/**
	 * kyukyuを取得します。
	 * @return kyukyu
	 */
	public int getKyukyu() {
	    return kyukyu;
	}
	/**
	 * kyukyuを設定します。
	 * @param kyukyu kyukyu
	 */
	public void setKyukyu(int kyukyu) {
	    this.kyukyu = kyukyu;
	}

}
